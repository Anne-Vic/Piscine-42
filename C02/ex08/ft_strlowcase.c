#include <unistd.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strlowcase(char *str)
{
	int i;
	int other;

	i = 0;
	other = 0;
	if (ft_strlen(str) == 0)
	{
		return (str);
	}
	while (str[i] != '\0')
	{
		if (str[i] > 64 && str[i] < 91)
		{
			str[i] += 32;
		}
		i++;
	}
	return (str);
}
